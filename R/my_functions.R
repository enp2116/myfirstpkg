#' Add Two Numbers
#'
#' This function adds two numeric values together and returns the result.
#'
#' @param a A numeric value representing the first number.
#' @param b A numeric value representing the second number.
#' @return A numeric value, which is the sum of `a` and `b`.
#' @export
#' @examples
#' add_numbers(2, 3)  # Returns 5
add_numbers <- function(a, b) {
  a + b
}

#' Subtract Two Numbers
#'
#' This function subtracts the second numeric value from the first and returns the result.
#'
#' @param a A numeric value representing the number to be subtracted from.
#' @param b A numeric value representing the number to subtract.
#' @return A numeric value, which is the result of `a - b`.
#' @export
#' @examples
#' subtract_numbers(5, 2)  # Returns 3
subtract_numbers <- function(a, b) {
  a - b
}


